package br.com.andreguedes.xti;

/**
 * Assignment operator
 * @author Andre
 *
 */
public class Aula17 {
	// Aula 017 - Operadores de atribuicao
	
	public static void main(String[] args) {
		int x = 6;
		x = x + 3;
		x += 3;
		
		System.out.println(x);
		
		x *= 2;
		x /= 2;
		x -= 2;
		x %= 2;
		
		System.out.println(x);
	}

}
